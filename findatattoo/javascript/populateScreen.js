function getShopIndex(shopRef) {
    let pos = -1;
    for (var i in data.shops) {
        if (data.shops[i].ref == shopRef)
            pos = i;
    }
    return pos;
}

function getArtistIndex(ref, shopRef) {
    let pos = -1;
    for (var i in data.artists) {
        if (data.artists[i].ref == ref && data.artists[i].shopRef == shopRef)
            pos = i;
    }
    return pos;
}


